def sumar(lista):
    # Suma todos los números en una lista.
    total = 0
    for num in lista:
        total += num
    return total

# Ejemplo
print(sumar([1, 2, 3, 4]))  # 10

def multiplicar(lista):
    # Multiplica todos los números en una lista.
    producto = 1
    for num in lista:
        producto *= num
    return producto

# Ejemplo
print(multiplicar([1, 2, 3, 4]))  # 24
